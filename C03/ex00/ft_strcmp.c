int	ft_strcmp(char *s1, char *s2)
{
	int	size_s1;
	int	size_s2;

	size_s1 = 0;
	size_s2 = 0;
	while ((s1[size_s1] != '\0') || (s2[size_s2] != '\0'))
	{
		if (s1[size_s1] != '\0')
		{
			size_s1++;
		}
		if (s2[size_s2] != '\0')
		{
			size_s2++;
		}
	}
	return (size_s1 - size_s2);
}
