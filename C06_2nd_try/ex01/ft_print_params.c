#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (i < argc)
	{
		while (argv[i][j] != '\0')
			j++;
		argv[i][j] = '\n';
		write(1, argv[i], (j + 1));
		i++;
		j = 0;
	}
	return (0);
}
