int	ft_sqrt(int nb)
{
	int	root;
	int	i;

	root = 0;
	i = 1;
	while (nb >= i)
	{
		nb = nb - i;
		root++;
		i += 2;
	}
	if (nb == 0)
		return (root);
	else
		return (0);
}
